import opendatasets as od

# Link do dataset p칰blico no Kaggle
dataset_url = "https://www.kaggle.com/datasets/karakaggle/kaggle-cat-vs-dog-dataset/data"

# Download do dataset
od.download(dataset_url)

"""## Manipulando um diret칩rio para nossas imagens"""

import os

base_dir = "kaggle-cat-vs-dog-dataset/kagglecatsanddogs_3367a/PetImages"
print("Pastas dispon칤veis:", os.listdir(base_dir))
print("Qtd de imagens em Cats:", len(os.listdir(os.path.join(base_dir, "Cat"))))
print("Qtd de imagens em Dogs:", len(os.listdir(os.path.join(base_dir, "Dog"))))

"""## Gerador de imagens com a normaliza칞칚o"""

from tensorflow.keras.preprocessing.image import ImageDataGenerator

# Criando gerador de imagens com normaliza칞칚o e data augmentation
datagen = ImageDataGenerator(
    rescale=1./255,
    validation_split=0.2,  # separa 20% para valida칞칚o
    rotation_range=30,
    width_shift_range=0.2,
    height_shift_range=0.2,
    shear_range=0.2,
    zoom_range=0.2,
    horizontal_flip=True
)

# Conjunto de treino
train_generator = datagen.flow_from_directory(
    base_dir,
    target_size=(150, 150),
    batch_size=32,
    class_mode="binary",
    subset="training"
)

# Conjunto de valida칞칚o
val_generator = datagen.flow_from_directory(
    base_dir,
    target_size=(150, 150),
    batch_size=32,
    class_mode="binary",
    subset="validation"
)

"""## Carregando o MobileNetV2

Deve-se usar o aprendizado previo com o congelamento da ultima camada, uma vez que essa constar치 a saida do resultado, para nos ser치 uma sa칤da bin치ria para gato, ou cachorro.
"""

import tensorflow as tf
from tensorflow.keras.applications import MobileNetV2
from tensorflow.keras import layers, models

# Carregar MobileNetV2 sem a camada final
base_model = MobileNetV2(weights="imagenet", include_top=False, input_shape=(150,150,3))
base_model.trainable = False  # congela as camadas do modelo base

# Construindo o modelo final
model = models.Sequential([
    base_model,
    layers.GlobalAveragePooling2D(),
    layers.Dense(128, activation="relu"),
    layers.Dropout(0.5),
    layers.Dense(1, activation="sigmoid")  # sa칤da bin치ria
])

# Compilar o modelo
model.compile(
    optimizer=tf.keras.optimizers.Adam(learning_rate=0.0001),
    loss="binary_crossentropy",
    metrics=["accuracy"]
)

model.summary()

"""# Treinando um modelo

O uso de apenas dez epocas, deve-se apenas ao fato de agilizar o treino, uma vez que o projeto tem caracteristica de apenas aprender, n칚o necessariamente gerando a melhor sa칤da.
"""

history = model.fit(
    train_generator,
    epochs=10,
    validation_data=val_generator
)

"""## Visualizando os resultados"""

import matplotlib.pyplot as plt

acc = history.history['accuracy']
val_acc = history.history['val_accuracy']
loss = history.history['loss']
val_loss = history.history['val_loss']

epochs = range(1, len(acc)+1)

plt.figure(figsize=(12,5))

plt.subplot(1,2,1)
plt.plot(epochs, acc, 'b', label="Treino")
plt.plot(epochs, val_acc, 'r', label="Valida칞칚o")
plt.title("Acur치cia")
plt.legend()

plt.subplot(1,2,2)
plt.plot(epochs, loss, 'b', label="Treino")
plt.plot(epochs, val_loss, 'r', label="Valida칞칚o")
plt.title("Perda")
plt.legend()

plt.show()

"""## Testando o comportamento do aprendizado"""

import numpy as np
from tensorflow.keras.preprocessing import image

# Pegando uma imagem de exemplo
img_path = os.path.join(base_dir, "Cat", os.listdir(os.path.join(base_dir,"Cat"))[0])
img = image.load_img(img_path, target_size=(150,150))

plt.imshow(img)
plt.axis("off")
plt.show()

# Pr칠-processar a imagem
x = image.img_to_array(img)
x = np.expand_dims(x, axis=0) / 255.0

# Fazer previs칚o
pred = model.predict(x)[0][4]
print("Predi칞칚o:", "Cachorro 游냤" if pred > 0.5 else "Gato 游냠")

model.save("cats_vs_dogs_transfer.h5")
